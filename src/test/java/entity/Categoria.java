package entity;

public class Categoria {
    private Long idCatagoria;
    private String name;

    public Long getIdCatagoria() {
        return idCatagoria;
    }

    public void setIdCatagoria(Long idCatagoria) {
        this.idCatagoria = idCatagoria;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
